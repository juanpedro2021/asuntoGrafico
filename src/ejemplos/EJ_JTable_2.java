    private void TablaAlumnosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaAlumnosMouseClicked
        
       int filaSele = TablaAlumnos.getSelectedRow();
       
        if (filaSele != -1) {
            
            int matricula = (Integer)TablaAlumnos.getValueAt(filaSele, 0);
            String apellido = (String)TablaAlumnos.getValueAt(filaSele, 1);
            String nombre = (String)TablaAlumnos.getValueAt(filaSele, 2);
            
            //cuardo cada cosa en su lugar
            
            infoMatricula.setText(matricula + "");
            infoApellido.setText(apellido );
            infoNombre.setText(nombre );
        }
        
    }//GEN-LAST:event_TablaAlumnosMouseClicked

    
