    private void cargarDatos(){
            
        ArrayList <Alumno> lista = new ArrayList<>() ;
        
        lista.add(new Alumno(123,"lopez","mariano"));
        lista.add(new Alumno(132,"noblex","laura"));
        lista.add(new Alumno(321,"sony","pedro"));
        lista.add(new Alumno(444,"acer","kiko"));
        
        for(Alumno alumno:lista){
            modelo.addRow(new Object[]{alumno.getMatricula(),
                alumno.getApellido(),alumno.getNombre()});

        }
        }
